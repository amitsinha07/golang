package main

import "fmt"

func main() {
	var username string = "Amit Kumar"
	fmt.Println(username)

	var isLoggedIn bool = false
	fmt.Println(isLoggedIn)

	var number int = 100
	fmt.Println(number)

	// default values and some aliases
	var anotherVariable int;
	var var2 string;
	fmt.Println(anotherVariable);
	fmt.Println(var2)

	var3 := 30;
	fmt.Println(var3)
}
